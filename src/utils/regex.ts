export const phoneRegexp =
  /^(((13[0-9]{1})|(15[0-9]{1})|(16[0-9]{1})|(17[3-8]{1})|(18[0-9]{1})|(19[0-9]{1})|(14[5-7]{1}))+\d{8})$/;

export const emailRegexp = /^\w+([-+.]\w+)*@\w+([-.]\w+)*\.\w+([-.]\w+)*$/;

/**
 * 只允许英文或英文+数字（长度至少6位），不能全为数字，不能包含其他字符
 * - 纯英文通过: 'abcdef', 'AbCdEf'
 * - 英文+数字通过: 'abc123', 'ABC123', 'a1b2c3'
 * - 全数字不通过: '123456'
 * - 含特殊字符不通过: 'abc123!', 'abc_def'
 */
export const passwordRegxp = /^(?:[a-zA-Z]{6,}|(?=.*[a-zA-Z])(?=.*\d)[a-zA-Z\d]{6,})$/;
